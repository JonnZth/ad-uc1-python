# Nome de Variavel
# -----------------------
# Regras gerais
# - não começar com numero (da erro)
# - não começar com caracter especial (conflito com função nativa da linguagem)
# - não começar com maiuscula
# - não pode usar uma palavra reservada
# - não pode ter espaço ' ' entre palavras
# - não pode usar acentuação
#
# Como usar a ideia de espaço no nome de variavel
# - snake_case
# - camelCase
# - PascalCase (tecnicamente pode, mas indica que não é uma variavel comum, é um objeto)
# 
# Constantes
# - TUDOEMMAIUSCULO, indica que é uma constante de forma não oficial, pois python não tem constante de fato